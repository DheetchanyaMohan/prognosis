"""Executes one full experiment run from a single validated RunConfig,
producing every artifact this subsystem owns (metrics.csv, training.log,
confusion_matrix.png, summary.json, diagnostics.json) and persisting the
run to SQLite.

diagnostics.json is generated by calling app.tools.metrics_analysis
directly after training completes — no diagnostic logic is duplicated
here. This is the same module future agent tools, the eval harness, and
the frontend will call against the same data.
"""

from __future__ import annotations

from pathlib import Path

import torch
from sqlalchemy.orm import Session

from app.config.schema import RunConfig
from app.data_generation.confusion_matrix import (
    compute_confusion_matrix,
    save_confusion_matrix_image,
)
from app.data_generation.datasets import build_dataloaders, build_train_val_datasets
from app.data_generation.metrics_writer import MetricsCsvWriter
from app.data_generation.model import SmallCNN
from app.data_generation.persistence import get_or_create_experiment, persist_run
from app.data_generation.seeding import seed_everything
from app.data_generation.summary import build_summary, write_summary
from app.data_generation.trainer import run_training
from app.data_generation.training_logger import build_run_logger
from app.tools.metrics_analysis import summarize_run, write_diagnostics


def execute_run(
    config: RunConfig,
    run_dir: Path,
    db: Session,
    device: torch.device | None = None,
) -> None:
    """Executes `config` end-to-end and writes every artifact under `run_dir`.

    `run_dir` is expected to already exist (created by
    scripts/generate_experiment_plan.py) with an `artifacts/` subfolder.
    """
    device = device or torch.device("cuda" if torch.cuda.is_available() else "cpu")

    metrics_path = run_dir / "metrics.csv"
    log_path = run_dir / "training.log"
    summary_path = run_dir / "summary.json"
    confusion_matrix_path = run_dir / "artifacts" / "confusion_matrix.png"
    diagnostics_path = run_dir / "diagnostics.json"

    if metrics_path.exists():
        metrics_path.unlink()  # MetricsCsvWriter appends; start clean on rerun

    seed_everything(config.seed)
    logger = build_run_logger(config.run_id, log_path)
    logger.info(f"Starting run {config.run_id}: {config.description}")

    train_dataset, val_dataset = build_train_val_datasets(
        train_size=config.dataset.train_size,
        val_size=config.dataset.val_size,
        augmentation=config.dataset.augmentation,
        seed=config.seed,
    )
    train_loader, val_loader = build_dataloaders(
        train_dataset, val_dataset, batch_size=config.training.batch_size, seed=config.seed
    )

    model = SmallCNN(dropout=config.model.dropout).to(device)
    metrics_writer = MetricsCsvWriter(metrics_path)

    result = run_training(model, train_loader, val_loader, config, device, metrics_writer, logger)

    confusion_matrix = compute_confusion_matrix(model, val_loader, device)
    save_confusion_matrix_image(confusion_matrix, confusion_matrix_path)

    status = "diverged" if result.diverged else "complete"
    summary = build_summary(
        config.run_id, result.epoch_history, result.wall_clock_sec, result.diverged
    )
    write_summary(summary, summary_path)

    diagnostics = summarize_run(config.run_id, result.epoch_history)
    write_diagnostics(diagnostics, diagnostics_path)

    logger.info(
        f"Finished run {config.run_id}: status={status}, "
        f"epochs_completed={len(result.epoch_history)}"
    )

    experiment = get_or_create_experiment(db, name=config.experiment_name)
    persist_run(
        db=db,
        experiment=experiment,
        run_id=config.run_id,
        config_path=str(run_dir / "config.yaml"),
        metrics_path=str(metrics_path),
        log_path=str(log_path),
        summary_path=str(summary_path),
        diagnostics_path=str(diagnostics_path),
        status=status,
        epoch_history=result.epoch_history,
    )